async function getConversations(userId) {
    const currentUser = await getUserById(userId);
    if (!currentUser) return {
        supervisor: {},
        peer: {}
    };
    const supervisorConversations = {};
    const peerConversations = {};
    if (currentUser.role === 'student') {
        if (currentUser.supervisorId) {
            const messages = await getSupervisorMessages(userId, currentUser.supervisorId);
            supervisorConversations[currentUser.supervisorId] = messages.filter((m)=>!m.deletedFor?.includes(userId));
        }
        if (currentUser.supervisorId) {
            const allStudents = await getStudentsBySupervisorId(currentUser.supervisorId);
            for (const student of allStudents){
                if (student.id === userId) continue;
                const conversationId = [
                    userId,
                    student.id
                ].sort().join('-');
                const messages = await getPeerMessages(conversationId);
                peerConversations[student.id] = messages.filter((m)=>!m.deletedFor?.includes(userId));
            }
        }
    }
    if (currentUser.role === 'supervisor') {
        const students = await getStudentsBySupervisorId(userId);
        for (const student of students){
            const messages = await getSupervisorMessages(student.id, userId);
            supervisorConversations[student.id] = messages.filter((m)=>!m.deletedFor?.includes(userId));
        }
    }
    return {
        supervisor: supervisorConversations,
        peer: peerConversations
    };
}
async function markSupervisorMessagesAsRead(currentUserId, otherUserId) {
    const currentUser = await getUserById(currentUserId);
    if (!currentUser) return;
    let studentId, supervisorId;
    if (currentUser.role === 'student') {
        studentId = currentUserId;
        supervisorId = otherUserId;
    } else {
        studentId = otherUserId;
        supervisorId = currentUserId;
    }
    const messages = await getSupervisorMessages(studentId, supervisorId);
    const messagesToUpdate = messages.map((m)=>m.senderId !== currentUserId ? {
            ...m,
            read: true
        } : m);
    if (messagesToUpdate.length > 0) {
        await updateSupervisorMessages(studentId, supervisorId, messagesToUpdate);
    }
}
